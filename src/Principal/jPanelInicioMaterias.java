    private void btnEliminarJPanelInicioMateriasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarJPanelInicioMateriasActionPerformed
         int selectedRow = tblListaMateriasJPanelInicioMaterias.getSelectedRow();  
    if (selectedRow == -1) {  
        JOptionPane.showMessageDialog(this, "Por favor, selecciona una materia para eliminar.");
        return;
    }

    // Aquí accedes a la columna 0 porque es donde está el nombre de la materia
    String nombreMateriaSeleccionada = (String) tblListaMateriasJPanelInicioMaterias.getValueAt(selectedRow, 0);
    
    // Eliminar la fila de la tabla
    DefaultTableModel modelo = (DefaultTableModel) tblListaMateriasJPanelInicioMaterias.getModel();
    modelo.removeRow(selectedRow);
    
    // Eliminar la materia del archivo
    eliminarMateriaDelArchivo(nombreMateriaSeleccionada);

    // Volver a cargar las materias en la tabla después de la eliminación
    cargarMateriasEnTabla();  
}

private void eliminarMateriaDelArchivo(String nombreMateria) {
    String ruta = "C:\\Users\\MI EQUIPO\\Desktop\\Trabajos U\\Simulacion de Sistemas\\Task Manager\\materias.txt";  
    File archivo = new File(ruta);

    List<String> lineasRestantes = new ArrayList<>();
    
    try (BufferedReader reader = new BufferedReader(new FileReader(archivo))) {
        String linea;
        
        while ((linea = reader.readLine()) != null) {
            String[] datos = linea.split(";");

            if (datos.length >= 2) {
                // Compara con el nombre de la materia (columna 0)
                if (!datos[0].equals(nombreMateria)) {  
                    lineasRestantes.add(linea);
                }
            }
        }

        try (BufferedWriter writer = new BufferedWriter(new FileWriter(archivo))) {
            for (String l : lineasRestantes) {
                writer.write(l);
                writer.newLine();
            }
        }

        JOptionPane.showMessageDialog(this, "Materia eliminada correctamente.");
    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al eliminar la materia: " + e.getMessage());
    }
    }//GEN-LAST:event_btnEliminarJPanelInicioMateriasActionPerformed

