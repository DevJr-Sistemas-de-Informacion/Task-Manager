public void cargarMateriasEnTabla() {
    String ruta = "C:\\Users\\MI EQUIPO\\Desktop\\Trabajos U\\Simulacion de Sistemas\\Task Manager\\materias.txt";
    DefaultTableModel modelo = (DefaultTableModel) tblListaMateriasJPanelInicioMaterias.getModel();
    modelo.setRowCount(0); // Limpiar la tabla

    try (BufferedReader reader = new BufferedReader(new FileReader(ruta))) {
        String linea;

        while ((linea = reader.readLine()) != null) {
            String[] datos = linea.split(";");
            if (datos.length == 2) {
                modelo.addRow(new Object[]{datos[0], datos[1]}); // Nombre y Descripción
            }
        }

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al cargar las materias: " + e.getMessage());
    }

    tblListaMateriasJPanelInicioMaterias.revalidate();
    tblListaMateriasJPanelInicioMaterias.repaint();
}
