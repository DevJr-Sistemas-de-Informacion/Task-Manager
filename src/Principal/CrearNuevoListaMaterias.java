    private void btnCrearNuevaMateriaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearNuevaMateriaActionPerformed
       String nombreMateria = txtNombreMateria.getText().trim();
    String descripcion = txtDescripcionMateria.getText().trim();

    if (nombreMateria.isEmpty() || descripcion.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Por favor, completa todos los campos.");
        return;
    }

    // Ruta del archivo para guardar materias
    String ruta = "C:\\Users\\MI EQUIPO\\Desktop\\Trabajos U\\Simulacion de Sistemas\\Task Manager\\materias.txt";

    try (BufferedWriter writer = new BufferedWriter(new FileWriter(ruta, true))) {
        writer.write(nombreMateria + ";" + descripcion);
        writer.newLine();

        JOptionPane.showMessageDialog(this, "Materia creada exitosamente.");

        // Limpiar campos
        txtNombreMateria.setText("");
        txtDescripcionMateria.setText("");

        // Actualizar tabla de materias
        SwingUtilities.invokeLater(() -> {
            panelMaterias.cargarMateriasEnTabla();
            panelMaterias.revalidate();
            panelMaterias.repaint();
        });

        // Cerrar la ventana
        this.dispose();

    } catch (IOException e) {
        JOptionPane.showMessageDialog(this, "Error al guardar la materia: " + e.getMessage());
    }
    }//GEN-LAST:event_btnCrearNuevaMateriaActionPerformed

